<h2>PRESENTATION DE L'ECOLE DOCTORALE</h2>
<p>
Le Doctorat constitue la troisième et dernière étape du cursus LMD. Il s'effectue en trois ans, après l'obtention d'un Master recherche ou, pour les étudiants étrangers, d'un  titre jugé équivalent.
</p>
<p>
Outre la participation active à des séminaires, ou des journées Doctorales, le doctorant a pour tâche essentielle l'élaboration d'une thèse, mémoire original dont le sujet est défini en accord avec un enseignant-chercheur accrédité par l'École doctorale d'Archéologie de l'Université Paris 1.
</p>
<p>
<h2>
LA DIVISION DES ETUDES DOCTORALES
</h2>
<p>
Une Ecole Doctorale est un regroupement d’équipes d’accueil et est définie par un périmètre scientifique. Les Ecoles doctorales sont rattachées à une ou plusieurs universités (suivant les établissements dont relèvent les équipes qui la composent).
</p>
<p>
Un étudiant s’inscrivant en doctorat est automatiquement rattaché, par le biais de son directeur de recherche à une équipe d’accueil et donc à une école doctorale. L’école Doctorale fixe les critères d’admission, conditions de réinscriptions, et parcours de formation que devront réaliser les doctorants jusqu’à l’obtention de leur diplôme. L’école doctorale permet à l’établissement dont elle relève de délivrer le diplôme national de doctorat dans l’un des champs scientifiques couvert par l’accréditation de l’Ecole Doctorale.
</p>
