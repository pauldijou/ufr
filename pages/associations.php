<h2>L'association Périples</h2>

<img class="logo-association" src="img/periples.jpg" width="259" height="198" />
<p>
  L'association Périples a été créée en novembre 2007 à l'instigation d'Adeline Grand- Clément et de 
  Sandra Péré-Noguès, toutes deux Maîtres de conférences en Histoire grecque.
  Les membres de son bureau et ses adhérents sont majoritairement des étudiants de Licence, de Master, des 
  doctorants et des personnes préparant les concours ; la plupart sont inscrits en Histoire ou en Histoire de 
  l'art-Archéologie.
</p>
<p>
  L'association, référencée comme l'une des associations de l'Université de Toulouse II-Le Mirail, a pour but 
  de promouvoir la connaissance des mondes anciens auprès de la communauté universitaire et s'appuie donc sur 
  le dynamisme du <a href="http://w3.histart-archeo.univ-tlse2.fr/master-recherche.html#SDA">Master en Sciences 
  de l'Antiquité.</a><br />
</p>
<p>
 Les membres de Périples se donnent pour objectif d'organiser diverses manifestations – conférences, expositions, 
 sorties culturelles – ainsi que des voyages d'étude en Méditerranée, en particulier pour les étudiants et jeunes 
 chercheurs ayant besoin de se rendre sur le terrain. Cette année, 53 personnes, professeurs et étudiants, iront 
 visiter certains sites de Grèce lors des vacances d'avril : Athènes, Mycènes, Corinthe, Delphes, Olympie.
</p>
<p>
 Depuis 2008, l'association se développe au sein de l'université et de l'UFR d'Histoire, Arts et Archéologie. 
 Ils espèrent fédérer ainsi autour de leurs projets de nombreux étudiants et enseignants intéressés par les 
 diverses civilisations qui se sont épanouies sur le pourtour de la Méditerranée antique.
</p>
<p>
 Contact : <a href="mailto:periples.utm@gmail.com">periples.utm@gmail.com</a>
</p>
<p>
  Une boîte aux lettres au nom de l'association se trouve dans la salle du courrier de l'UFR d'Histoire, Arts 
  et Archéologie.
</p>

<h2>Le Foyer de l'UFR Histoire, Arts et Archéologie</h2>

<img class="logo-association" src="img/foyer.jpg" width="186" height="248" />
<p>
  <a href="http://ufrhistoire.clicforum.fr/portal.php">http://ufrhistoire.clicforum.fr</a>
</p>
<p>
  « Une présentation du foyer » par Sébastien Poublanc :
</p>
<p>
  &quot;Petite description du Foyer pour ceux, et ils sont nombreux, qui ne le connaissent pas...
</p>
<p>
  Tout d'abord, une légère précision géographique (certains diront archéologique...) : le Foyer est situé dans 
  le sous-sol de l'UFR Histoire, Art et Archéologie... J'en vois certains qui font de gros yeux incrédules… et 
  oui, nous sommes relégués au sous-sol. Mais nous n'y perdons pas au change: nous avons un grand local partagé 
  entre tables pour se réunir, travailler, discuter et canapés/fauteuils pour finir une nuit, lire une BD, jouer 
  aux jeux de société ou encore discuter.
</p>
<p>
  Depuis 2007, le lieu de vie est devenu entièrement non fumeur, pour le plus grand plaisir de certains, et pour 
  le déplaisir des autres… Nous servons toutes sortes de boissons (café, thés de différents parfums, cappuccino, 
  sirops), possédons une énorme collection de bds à votre disposition, passons de la musique et sommes là pour 
  vous aider dans la mesure de nos moyens.&quot;
</p>
<p>
  &quot;Il ne s'agit pas que du Foyer d'Histoire, mais bel et bien du Foyer d'Histoire, Art et Archéologie, où 
  tous les étudiants de ces matières peuvent y trouver un coup de main. Ah oui, précision importante: les 
  membres du Foyer sont des étudiants comme vous qui prennent sur leur temps libre pour tenir des permanences. 
  Ils ne sont en aucun cas rémunérés, tout au plus ont-ils des réductions sur le café, ce qui, à la réflexion, 
  n'est pas si mal...
</p>
<p>
  Foyer étudiant, nous essayons dans la mesure de nos moyens d'organiser des manifestations plus ou moins 
  culturelles. Donc si vous êtes à la recherche d'une galerie pour exposer vos œuvres, venez nous voir, la 
  salle est là pour ça, nous vous exposons et vous payons même les tirages si besoin en est...<br />
  Un enseignant fantôme ? Une salle introuvable ? Peut-être que nous pourrons vous aider, la plupart des 
  étudiants qui séjournent au Foyer sont déjà formés aux rudes aléas de la vie universitaire et sauront vous 
  aider...
</p>
<p>
  Enfin pour conclure cette longue présentation, je ne saurai que trop vous inciter à vous rendre au Foyer. 
  La fac étant par nature déshumanisante (entre les amphis surpeuplés, les profs qui nous considèrent souvent 
  comme des numéros...), le Foyer se veut un lien entre les étudiants, apolitique et chaleureux (enfin, a 8h 
  du mat', peut-être que le permanencier manque de chaleur, mais ça peut être compréhensible...). D'ailleurs, 
  le Foyer recrute toute bonne volonté pour rajeunir ses cadres vieillissants (pas proches de la retraite, 
  plutôt du monde du travail...) aussi nous vous attendons.
</p>
<p>
  Pour aller au Foyer, il vous suffit d'entrer dans l'UFR: il y a à l'entrée une sorte de sas vitré; la porte 
  du Foyer se trouve tout à droite, il suffit de descendre les escaliers pour y accéder...&quot;
</p>

<h2>APAREA : Action de Promotion et d'Aide à la Recherche en Archéologie</h2>
<img class="logo-association hidden-phone" src="img/logo-APAREA.gif" width="350" height="212" /> 
  
<p>
  <a href="http://aparea.free.fr/">http://aparea.free.fr/</a>
</p>
<p>
  Ouverte à tous, elle se propose d'encourager la vocation archéologique des étudiants et du grand public par :
</p>
<ul style="text-align: justify;">
  <li>Un soutien aux étudiants de 1er et 2nd cycle en archéologie (ou autres) concernant leurs travaux et/ou 
  l'élaboration de leur mémoire, par le biais de conseils, de rencontres, etc.</li>
  <li>La promotion de l'archéologie auprès du grand public par la mise en place de stages, conférences ou 
  expositions dans les écoles, villages...</li>
  <li>L'édition d'un bulletin régulier abordant l'actualité de la recherche et autres sujets autour de 
  l'archéologie.</li>
  <li>La mise en ligne d'un site internet qui se veut être une plateforme pédagogique, pourvue d'une base 
  de données bibliographique et de pleins d'autres choses toutes plus indispensables les unes que les autres.</li>
  <li>Le versement d'une aide financière symbolique pour l'impression des mémoires universitaires.<br />
</ul>
  
<h2>Les autres associations</h2>

<p>Les études supérieures constituent un moment privilégié d'ouverture sur le monde et sur les autres.</p>
<p>L'acquisition de savoirs et de méthodes, la culture, le sport et l'engagement associatif contribuent 
fortement à l'épanouissement de la personnalité. Participer à des activités culturelles, pratiquer un sport, 
animer un foyer étudiant, s'investir dans une association, sont autant d'actes de responsabilité et d'implication 
qui favorisent l'insertion de tous dans la vie universitaire, et le développement de l'autonomie de chacun.</p>
<p>Consciente de l'intérêt que représentent ces activités, l'Université de Toulouse le Mirail s'est engagée de 
façon volontariste dans une politique de soutien aux initiatives étudiantes et aux associations étudiantes. 
Un des éléments de cette politique volontariste est la création de la commission &quot;vie universitaire&quot;. 
Ses missions principales sont de contribuer à l'amélioration de la vie sur le campus et, avec le concours de 
tous les acteurs de la communauté universitaire, de dynamiser la vie associative et citoyenne. Elle est composée 
à parité d'étudiants (membres des associations, des foyers et de la MDE (Maison des Etudiants)), et de personnel 
enseignant ou non.</p>
<p>Christian Mange (rapporteur de la Commission Vie Universitaire) - sept. 2004</p>
