<h1>Master Histoire de l'art et patrimoine : moderne et contemporain</h1>
<p>Emploi du temps Master I Histoire de l'art et Patrimoine : Moderne</p>
<p>Emploi du temps Master I Histoire de l'art et Patrimoine : Contemporain</p>
<p>
Cette formation vise à fournir les bases d'une autonomie intellectuelle, méthodologique et technique, aussi 
bien pour la recherche que pour des pratiques professionnelles.
</p>

<p> 
<img src="img/buren-img-masterpatrimoine.jpg" width="400" height="300" class="left" />
Le but est de permettre à l'étudiant de mener seul -mais aussi en équipe-  une recherche théorique, tout 
en l’armant pour la réflexion, la formulation puis la valorisation de son travail. La formation implique 
la maîtrise de la recherche bibliographique, documentaire et iconographique ainsi que la pratique de langues 
contemporaines et parfois anciennes. L'objectif est de se familiariser avec les outils de la recherche 
-traditionnels et numériques- mais aussi de tester puis d’amplifier les capacités d'analyse, de réflexion, 
de synthèse et de rédaction en construisant un discours analytique et problématique fondé sur un corpus. 
L’acquisition de connaissances et l’apprentissage d’un esprit critique, en faisant largement appel à l’initiative 
personnelle, enrichissent une nécessaire ouverture d’esprit. </p>

<img src="img/conegliano-img-masterpatrimoine.jpg" width="305" height="371" class="right" />
<p>
  Stages et enseignements spécifiques permettent d’acquérir une large connaissance, théorique et concrète, 
  des secteurs d’activité liés à l’art, au patrimoine et à la recherche, afin d’aider l’étudiant à s’orienter. 
  La maîtrise du discours, oral et écrit, se double des capacités à aboutir à une publication originale et 
  personnelle, en respectant les méthodes nécessaires à la rédaction d'une publication scientifique moderne.
</p>

