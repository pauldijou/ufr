<h2>Les options de langues vivantes</h2>
<p>
Tout étudiant à l'université qui n'est pas inscrit en langues vivantes en discipline associée doit s'inscrire dans une langue vivante afin de valider les UE correspondant au niveau B2 du cadre européen commun de références en langues, nécessaire à l'obtention d'un Master. Les directives européennes ont conduit à la création d'un dispositif où des niveaux de langue correspondent à l'évaluation d'une capacité à la communication linguistique dans une langue donnée tout en tenant compte de la pratique de l'écrit et de l'oral. 
Il existe 6 niveaux de langue : A1 et A2 (initiation) ; B1 et B2 (perfectionnement) ; C1 et C2 (maîtrise de la langue).
