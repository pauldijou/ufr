<h1>La Licence</h1>
<p>
  La licence d’Histoire de l’art et Archéologie est conçue en lien avec la Recherche dans la perspective d’assurer une formation générale solide tant en Histoire de l’art qu’en Archéologie. 
</p>
<p>
L’enseignement, qui s’articule autour de quatre grandes périodes : ancienne, médiévale, moderne et contemporaine, est associé à l’apprentissage d’une méthodologie spécifique qui permet aux étudiants de développer des capacités d’analyse, de sens critique et de formation du goût. 
<br />
L'enseignement se veut aussi suffisamment interdisciplinaire par ses outils et ses options, pour qu’à l’issue des trois années de licence, les étudiants puissent trouver un débouché professionnel.
</p>
<p>
La licence d’Histoire de l’art et Archéologie prépare, en effet, à des poursuites d’études et offre des débouchés dans différents domaines.
<br />
L’obtention de la licence donne la possibilité de continuer des études en Masters recherche ou professionnel. 
<br />
Elle prépare également aux concours de la fonction publique d’Etat et des collectivités territoriales.
<br />
Enfin, le secteur privé offre des débouchés aux étudiants ayant acquis une expérience professionnelle ou complété 
leur formation par une spécialité (tourisme culturel, édition, métiers du patrimoine et de la culture, marché et métiers de l’art, documentation et régie des oeuvres, métiers liés à l'archéologie préventive, etc.).
</p>

<p>
 La licence est un diplôme qui s'acquiert en 3 ans (L1, L2, L3), soit 6 semestres. Chaque Unité d'enseignement (UE) 
 correspond à un nombre de crédits européens (ECTS : European Credits Transfer System).
 Pour obtenir la Licence, l'étudiant doit acquérir 180 ECTS (30 par semestre).
</p>
<p>
 <strong>Aucune UE du département d'Histoire de l'Art et Archéologie n'est disponible avec le SED 
 (Service d'Enseignement à Distance).</strong>
</p>
