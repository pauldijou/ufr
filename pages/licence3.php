<h1>La Licence 3</h1>
<p>
  La troisième année permet d’approfondir les connaissances et les compétences acquises aux cours des deux 
  premières années de la licence.
</p>
<p>
Durant cette année, les étudiants ont la possibilité de se spécialiser en Histoire de l’art ou en Archéologie, 
en choisissant l’un de ces deux parcours.
</p>
<p>
Cette formation s’appuie sur les UE « accompagnement/ projet » destinées à faire le lien entre les connaissances, 
la Recherche et le projet personnel de l’étudiant.
<br />
 Des stages (musées, chantiers de fouilles, bibliothèques, laboratoires de recherche...) peuvent compléter cette 
 formation.
</p>
