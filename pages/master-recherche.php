<h1>Le Master Recherche</h1>

<p>
  Dans le cadre du Master Recherche, le département propose quatre spécialités : 
</p>
<ul>
  <li>Arts et cultures de la Préhistoire et de la Protohistoire : Europe, Afrique</li>
  <li>Science de l'Antiquité</li>
  <li> Etudes médiévales</li>
  <li>Histoire de l'art et patrimoine : moderne et contemporain</li>
</ul>

<p>
  <img src="img/extasesttherese-img-masterrecherche.jpg" width="196" height="350" class="left" />
</p>
<p>
  Les liens établis avec de nombreux acteurs du monde professionnel et institutionnel (Services régionaux de l’inventaire, DRAC, Atelier municipal de restauration de Toulouse) garantissent un enseignement concret de haute qualité. De plus, les collaborations étroites nouées avec les institutions locales (Musée des Abattoirs, musée des Augustins) et nationales (Institut National d’Histoire de l’Art, Centre de recherche du château de Versailles) ont permis la mise en place de projets collectifs importants plaçant les étudiants au centre d’une dynamique de la recherche d’envergure. 
</p>
<p>
  Les secteurs d’activité visés sont ceux liés à la recherche, au patrimoine et à l’activité artistique, tant dans le privé que dans le public. Il s’agit de former directement ou d’initier : des enseignants et des chercheurs, pour ceux qui poursuivent en doctorat des experts dans le domaine de la science des archives des experts en recherche iconographique et documentaire des spécialistes des métiers de l'art, susceptibles de parfaire une formation dans les domaines de la restauration, de la conservation ou de la gestion des biens culturels. des acteurs de la vie culturelle contemporaine, des guides, des conférenciers, des médiateurs… des acteurs du marché de l'objet d'art et de son contrôle, vente, expertise, estimation… des acteurs des métiers de la diffusion culturelle et de l'édition des acteurs des institutions patrimoniales (DRAC, collectivités locales) et muséales. 
</p>

