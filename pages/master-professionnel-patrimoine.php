<h1>Master Professionnel</h1>

<h2>Présentation</h2>
<p>
À la rentrée 2011-2012, l'université de Toulouse-Le Mirail ouvre à Cahors la première année (M1) d’un master professionnel « Patrimoine » ; le M2 correspondant ouvrira à la rentrée suivante (2012-2013).
Le master patrimoine est une formation à effectif contingenté. Le nombre d’étudiants est fixé à 20 pour le M1 et 15 pour le M2.
Ce diplôme succède au master 2 professionnel « Histoire de l’art et patrimoine » qui était établi à Cahors depuis 1992. Il en conserve l’esprit, et s’appuie sur le rayonnement de celui-ci et l’expérience acquise durant dix-neuf années de fonctionnement.
Le Conseil général du Lot, la Communauté de communes du pays de Cahors et la Ville de Cahors ont renouvelé leur soutien à la formation dans le cadre d’un projet de développement d’un « Pôle patrimoine » à Cahors.
</p>
<p>
Le nouveau master patrimoine reste axé sur un véritable souci de professionnalisation : nombreux intervenants professionnels, visites, voyages d’études, stages. Il conserve le point fort du précédent master : la conduite par chaque promotion d’un projet professionnel collectif, dont la réalisation sera le fil rouge des deux années de formation.
</p>
<p>
L’année de M1 est essentiellement consacrée à une première approche de la notion de patrimoine et de son histoire, ainsi qu’à une formation à la recherche (ouverture pluridisciplinaire, méthodes, outils) et à ses applications dans le domaine professionnel (documentation, inventaire). La première phase du projet collectif en M1 est donc naturellement celle de la recherche sur le sujet proposé, qui touche au patrimoine lotois.
</p>
<p>
L’année de M2 est axée sur les aspects de valorisation et de médiation du patrimoine, tant dans les cours et les interventions que pour le projet collectif.
</p>
<p>
Un stage de huit à dix semaines en milieu professionnel se déroule au troisième trimestre de chaque année de master. Les missions de stage doivent porter sur les aspects de recherche, documentation, inventaire en M1, sur la valorisation et la médiation en M2.
</p>

<h2>Compétences visées</h2>
<p>
La formation vise à doter les diplômés de compétences polyvalentes, leur permettant de prendre en charge les différents aspects de l’exercice professionnel, d’assurer des fonctions de direction, et d’évoluer dans leur emploi. En résumé, le master Patrimoine propose d’acquérir :
</p>
<p> 	 	
une vision globale des problèmes patrimoniaux
une réflexion théorique et déontologique
les méthodologies et outils professionnels
une connaissance des milieux professionnels
une première expérience professionnalisante (projet professionnel collectif)
</p>
<h2>Débouchés professionnels</h2>
<p>
Le Master professionnel Histoire de l'art et Patrimoine ouvre sur divers types de métiers :
</p>
<p>	 	
chef de projet patrimonial ou chargé d’études en ingénierie culturelle et touristique, dans les secteurs public et privé ;
concours de la filière culturelle de la fonction publique territoriale et d’État : conservateur, attaché de conservation, assistant et assistant qualifié de conservation, animateur du patrimoine, chargé d’études documentaires… ;
métiers émergents, en particulier liés aux nouvelles collectivités et aux nouvelles technologies.
