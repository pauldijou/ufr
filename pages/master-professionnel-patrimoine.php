<h2>Candidatures</h2> 	 
<p>	 	
  NOUS NE DÉLIVRONS PAS DE DOSSIER DE CANDIDATURE. LE DOSSIER D'INSCRIPTION EST REMIS APRÈS L'ADMISSION
</p>
<p>
Le master patrimoine est une formation à effectif contingenté. Le nombre d’étudiants est fixé à 20 pour le M1 et 15 pour le M2.
Les candidats au master 1 professionnel « Patrimoine » doivent être titulaires d'une licence, (ou d’un M1 ou M2) d'histoire, d’histoire de l'art ou d’archéologie. Ils doivent déposer ou adresser auprès du secrétariat du département Histoire de l'art et Archéologie uniquement un dossier de candidature composé comme suit :
</p>
<p>
une lettre de candidature exposant les motivations personnelles du candidat,
un curriculum vitæ détaillé (contenu du cursus universitaire, des expériences patrimoniales, culturelles, associatives).
</p>
<p>
la copie des diplômes obtenus avec indication des mentions.
</p>
<p>
la liste des candidatures déposées dans les différentes universités.
</p>
<p>
une enveloppe timbrée à l'adresse du candidat pour convocation à l'entretien d'admission (prière d'indiquer un numéro de téléphone et/ou une adresse e-mail).
</p>
<p>
Un jury examine les dossiers reçus et sélectionne les étudiants convoqués pour un entretien d'admission avec un second jury début juillet. Ce jury établit la liste des étudiants retenus, proposée au président de l'université pour l'admission. Pour les étudiants qui n’auraient pas obtenu leur licence avant le jury d’admission, l’admission définitive ne sera prononcée qu’après production d’une attestation de réussite en licence.
</p>
<p>
Le premier jury établit son choix en fonction de la qualité des dossiers universitaires et de l'implication dans des activités culturelles, artistiques ou associatives : il est donc dans l’intérêt du candidat de lui fournir des informations aussi complètes que possible.
</p>	 	 