<h1>La Licence bi-disciplinaire</h1>
<p>
Disciplines complémentaires, l’histoire, l’histoire de l’art et l’archéologie sont désormais associées dans une même formation qui s’adresse, en particulier, aux étudiants intéressés par la recherche et/ou les métiers de la Culture et du Patrimoine.
</p>
<p>
Riche et diversifiée, l’offre de formation de la Licence bi-disciplinaire s’appuie en grande partie sur les Licences d’Histoire d’un côté et d’Histoire de l’art et Archéologie de l’autre dont elle reprend notamment les modules d’enseignement fondamental, en même temps qu’elle propose une série d’UE « mixtes » qui lui sont propres dans les quatre périodes de l’histoire (Ancienne, Médiévale, Moderne et Contemporaine). Ces cours, dispensés par des enseignants relevant des deux Départements, permettent à l’étudiant de toucher de près à la transdisciplinarité (croisement des sources, méthodologies propres aux disciplines associées). Les étudiants y trouveront ainsi toutes sortes d’outils et de méthodes d’analyse pour comprendre les mondes passés et actuel.
</p>
<p>
<img src="img/delacroix-img-licencebi.jpg" width="468" height="374" class="right"/>
</p>
<p>
  La licence bi-disciplinaire Histoire, Histoire de l’art et Archéologie est un diplôme généraliste qui prépare à des poursuites d’études dans différents domaines, que ce soit dans le cadre des masters recherche, que ce soit dans le cadre de formations plus professionnalisantes tels que les masters « métiers de l’enseignement » ou « métiers du patrimoine ». Elle prépare également aux concours de la fonction publique d’Etat, de la fonction publique européenne et de la fonction publique territoriale, notamment en catégories A et B. Cette licence ouvre aussi des perspectives dans les métiers du tourisme.
</p>
<p>
La licence est un diplôme qui s'acquiert en 3 ans (L1, L2, L3), soit 6 semestres. Chaque Unité d'enseignement (UE) correspond à un nombre de crédits européens (ECTS : European Credits Transfer System).
Pour obtenir la Licence, l'étudiant doit acquérir 180 ECTS (30 par semestre).
</p>
<p>
<strong>La licence bi-disciplinaire Histoire - Histoire de l'art est rattachée administrativement au département d'Histoire. Rendez-vous sur <a href="http://w3.histoire.univ-tlse2.fr/licencebi.html">le site du département d'Histoire</a> pour des informations complémentaires, les procédures d'inscription ainsi qu'une présentation des cours.</strong>
</p>
