<h1>Master Sciences de l'Antiquité</h1>
<p>
  La formation Sciences de l'Antiquité, dont les fondations s'appuient, pour une part, sur l'expérience d'un DEA « Sciences de l'Antiquité » a l'ambition de rassembler tous les champs du savoir qui permettent l'étude la plus large et la plus approfondie de l'Antiquité, c'est-à-dire des civilisations du pourtour de la Méditerranée et des héritages qui en sont issus et constituent le principal fondement culturel de l'Europe. Cette approche résolument transdisciplinaire réunit, de façon cohérente, les apports croisés de l'histoire, des langues et littératures anciennes, de l'archéologie, de l'histoire de l'art et de l'anthropologie, et prend appui sur les programmes de recherche de deux équipes et sur les compétences des enseignants-chercheurs qui y travaillent :
