Les deux premières années sont généralistes et reposent sur l’acquisition de savoirs et de savoirs faire généraux propres à l’Histoire de l’art et à l’Archéologie. </p>
<img src="img/florence-img-licence1.jpg" width="300" height="175" class="right" />
<p>
Les cours, qui reposent sur la projection et l’étude des œuvres d’art, visent à fournir à l’étudiant un large champ de connaissances chronologiques dans le domaine de l’évolution des arts et des civilisations de la Préhistoire à l’époque contemporaine, en abordant les grands thèmes et les productions artistiques.
</p>
<p>
<img src="img/bustestraymond-img-licence1.jpg" width="185" height="245" class="left" />
</p>
<p>
  Ces cours sont étroitement liés à un enseignement méthodologique (UE 3A) destiné à consolider des compétences acquises au lycée, à initier les étudiants au travail universitaire et à leur donner les outils qui leur seront utiles tout au long de leur cursus en Histoire de l’art et Archéologie. 
  Ces cours ont aussi pour objectif d’aider les étudiants à se familiariser avec la recherche documentaire, les NTIC (Nouvelles technologies de l’information et de la communication) notamment.
</p>
