  </div>
  <div class="span7">
   <p>
     FRA.M.ESPA (France Méridionale et Espagne), qui regroupe les chercheurs et enseignants-chercheurs de plusieurs 
     UFR : Histoire, Arts et Archéologie, Département d'Espagnol de l'UFR Langues, Département Linguistique de 
     l'UFR Lettres, en particulier.
   </p>
 </div>
</div>
<p>
  Quatre équipes sont particulièrement tournées vers le Moyen Age :
</p>
<ul>
	<li>Equipe 2 - La construction de l’espace rural des régions méridionales</li>
	<li>Equipe 3 - Féodalité, communautés et pouvoirs</li>
	<li>Equipe 4 - Religions, cultures et pouvoirs</li>
	<li>Equipe 6 - Al-Andalus-Hispaniae. Histoire, archéologie et littératures des sociétés ibériques au Moyen Age (VIe-XVe siècles)</li>
</ul>
