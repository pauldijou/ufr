<p>
  Terrae, coordination mise en place par les Equipes « Espace rural » et « Féodalité » de FRA.M.ESPA et par 
  l’Equipe « Archéologies des espaces médiévaux » de T.R.A.C.E.S., afin de mettre en oeuvre des programmes 
  scientifiques communs, prolongeant ainsi le rapprochement des disciplines médiévales opéré dans le master.
  Les étudiants bénéficient ainsi d'un encadrement et de propositions de recherche reflétant cette synergie.
</p>

