<h1>Master Etudes médiévales</h1>

<p>Accéder à la plaquette de présentation 2011-2012</p>

<p>
  L'originalité de la formation de Master spécifiquement consacrée au Moyen-Age est de conjuguer des approches très variées et de permettre à l'étudiant de découvrir de nombreuses disciplines: il pourra acquérir une ample formation associant :
</p>
<ul>
	<li>l'histoire</li>
	<li>l'histoire de l'art</li>
	<li>l'archéologie</li>
	<li>tout en glanant des enseignements complémentaires en littérature, linguistique, ethnologie ou anthropologie.</li>
</ul>

<p>
Les Etudes médiévales s’appuient essentiellement sur deux Unités Mixtes de Recherche de l’université. En effet, les médiévistes des départements d’histoire et d’histoire de l’art et archéologie participent à plusieurs des programmes de recherche de ces deux UMR, programmes qui définissent les volets du projet scientifique auxquels les étudiants en master se raccordent :
</p>
<div class="row" style="margin-bottom: 15px">
  <div class="span2" style="text-align: center;">
