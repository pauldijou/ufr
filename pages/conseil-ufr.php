<h1>Le Conseil de département</h1>

<p> 
Le département est administré par un Conseil élu pour 4 ans.
Il est dirigé par un Directeur de département, élu par le Conseil. Le directeur est responsable du centre de responsabilité budgétaire que constitue le département.
Le Conseil prépare et vote le budget du département qu’il soumet au Conseil d’UFR qui arrête le budget de l’UFR et l’enveloppe budgétaire de chaque département.
<br />
<em>(extrait des statuts de l'Université du 08/07/1997)</em>
</p>
<p>
  Les membre du Conseil sont répartis en collèges :
</p>
<dl>
  <dt>Collège des enseignants-chercheurs :</dt>
  <dd>Collège A : professeurs et personnels assimilés<br/>Collège B : autres enseignants et assimilés</dd>
  
  <dt>Collège des usagers</dt>
  <dd>Etudiants : inscrits dans le département, bénéficiant de la formation continue et auditeurs</dd>
  
  <dt>Collège IATOSS</dt>
  <dd>Personnels ingénieurs, administratifs, techniques, ouvriers de service et de santé.</dd>
</dl>
 
